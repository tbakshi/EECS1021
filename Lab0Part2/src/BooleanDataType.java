
public class BooleanDataType {

	public static void main(String[] args) {
		// The boolean data type consits of only two values: true or false
		boolean p = true; 
		boolean q = false; 
		
		System.out.println("p is " + p); 
		System.out.println("q is " + q); 
		System.out.println("After re-assigning p to false and q to true"); 

		p = false; 
		q = true; 
		
		System.out.println("p is " + p); 
		System.out.println("q is " + q); 
		
	}

}
